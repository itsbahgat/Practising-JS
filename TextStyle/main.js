
var p = document.body.firstElementChild;


function ChangeFont(font){
    p.style.fontFamily = font;
};
function ChangeAlign(alignment){
    p.style.textAlign = alignment;
};
function ChangeHeight(h){
    p.style.lineHeight = h;
};
function ChangeLSpace (s){
    p.style.letterSpacing = s;
};
function ChangeIndent (i){
    p.style.textIndent = i;
};
function ChangeDecorate (d){
    p.style.textDecoration = d;
};
function ChangeBorder (b){
    p.style.border = b;
};
function ChangeBorderColor (c){
    p.style.borderColor = c;
};
function ChangeTransform(t){
    p.style.textTransform = t;
}